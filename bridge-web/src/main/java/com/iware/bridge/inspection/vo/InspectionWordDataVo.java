package com.iware.bridge.inspection.vo;

/**
 * word所需数据类
 */
public class InspectionWordDataVo {

    private Integer id;

    private String structureName;

    private String inspectionYear;

    private String inspectionMonth;

    private String inspectionDate;

    private Integer inspectionDay;

    private String inspectionWeather;

    private String inspectionEve;

    private String inspector;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getStructureName() {
        return structureName;
    }

    public void setStructureName(String structureName) {
        this.structureName = structureName;
    }

    public String getInspectionYear() {
        return inspectionYear;
    }

    public void setInspectionYear(String inspectionYear) {
        this.inspectionYear = inspectionYear;
    }

    public String getInspectionMonth() {
        return inspectionMonth;
    }

    public void setInspectionMonth(String inspectionMonth) {
        this.inspectionMonth = inspectionMonth;
    }

    public String getInspectionDate() {
        return inspectionDate;
    }

    public void setInspectionDate(String inspectionDate) {
        this.inspectionDate = inspectionDate;
    }

    public Integer getInspectionDay() {
        return inspectionDay;
    }

    public void setInspectionDay(Integer inspectionDay) {
        this.inspectionDay = inspectionDay;
    }

    public String getInspectionWeather() {
        return inspectionWeather;
    }

    public void setInspectionWeather(String inspectionWeather) {
        this.inspectionWeather = inspectionWeather;
    }

    public String getInspectionEve() {
        return inspectionEve;
    }

    public void setInspectionEve(String inspectionEve) {
        this.inspectionEve = inspectionEve;
    }

    public String getInspector() {
        return inspector;
    }

    public void setInspector(String inspector) {
        this.inspector = inspector;
    }
}
